package com.adojos.nrao;

public class CompactAssignment {

    public static void main (String args[]) {

        int x = 5;
        x *= 10; // ******** (x = x*10)
        System.out.println(x);

        int y = 5;
        y += 10; // ******** (y = y+10)
        System.out.println(y);

        int z = 20;
        z -= 10; // ******** (z = z-10)
        System.out.println(z);

    }
}
